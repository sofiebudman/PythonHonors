def oval(bigR,smallR):
    turtle.circle(bigR,90)
    turtle.circle(smallR,90)
    turtle.circle(bigR,90)
    turtle.circle(smallR,90)
   

def rect(x,y,turn):
    for i in range(2):
        turtle.forward(x)
        turtle.right(turn)
        turtle.forward(y)
        turtle.right(turn)

def curve(isRight,scope,degree,step):
    for i in range (scope):
        if (isRight):
            turtle.right(degree)
        else:
            turtle.left(degree)
        turtle.forward(step)
def line(angle,length):
    turtle.seth(angle)
    turtle.fd(length)

